# Why eggs is valid:
# Eggs is a valid variable name in Python.
# It consists of letters, which are allowed in variable names.
# It does not contain any invalid characters like spaces or special symbols (except _).
# Why 100 is invalid:
# Variable names cannot start with a number.
# 100 is purely numeric, and Python treats it as an integer, not a variable name.
# If you try to assign a value like 100 = "value", Python will throw a SyntaxError.
