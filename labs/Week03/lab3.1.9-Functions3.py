# int() – Converts a value to an integer.
int("1")   # Converts string "10" to integer 10
int(1.5)   # Converts float 10.5 to integer 10 (truncates decimal part)

# float() – Converts a value to a floating-point number.
float("1.5")  # Converts string "10.5" to float 10.5
float(1)      # Converts integer 10 to float 10.0

# str() – Converts a value to a string.
str(1)     # Converts integer 10 to string "10"
str(1.5)   # Converts float 10.5 to string "10.5"
